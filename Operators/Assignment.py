x = 10
y = 3
x += y # x is now 13 bocz x=x+y ie..10+3=13
print('Addition : x=x+y',x) # x value is 13
x -= y # x is now 10, x was updated to 13 previously
print('Substraction : x=x-y',x) # x value is 10
x *= y # x is now 10
print('Multiplication : x=x*y',x) # x is 30,bcoz 10X3 =30
x /= y # x is now 30
print('Division : x=x/y',x) # 30/3=10
x %= y # x is now 10.0
print('Modulus : x=x%y',x) # x value is 3 10/3 modulus is 1.0
x **= y # x is now 1.0
print('Exponential : x=xXxXx',x) #x is now 1 bcox x should be multi by 3 times
x //= y # x is now 333.0
print('Floor Value : x=x//y',x) # x vlaue is 0.0
